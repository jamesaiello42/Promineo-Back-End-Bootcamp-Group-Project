package entity;

public class Users {
	private int id;
	private String userName;
	private String email;
	private String password;
	
	public int getId() {
		return id;
	}
	public String getUserName() {
		return userName;
	}
	public String getEmail() {
		return email;
	}
	public String getPassword() {
		return password;
	}
	
	
}
